/// General spacing scale for the Sav Design System.
///
/// Component-specific measurements (e.g. button height/padding) live with that
/// component's tokens rather than here.
abstract final class AppSpacing {
  static const none = 0.0;
  static const xxs = 2.0;
  static const sm = 4.0;
  static const md = 8.0;
  static const lg = 12.0;
  static const xl = 16.0;
  static const xxl = 20.0;
}
